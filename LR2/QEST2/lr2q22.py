x = float(input("Введіть число: "))

if x > 0:
    print(f"sign({x}) = 1")
elif x < 0:
    print(f"sign({x}) = -1")
else:
    print(f"sign({x}) = 0")

# Приклад вхідних і вихідних даних:
# Вхід:
# 5
# Вихід:
# sign(5.0) = 1
#
# Вхід:
# -3
# Вихід:
# sign(-3.0) = -1
#
# Вхід:
# 0
# Вихід:
# sign(0.0) = 0
